// 集合
import { BST } from '../tree/BST.js';

export class Set extends BST {
    constructor () {
        super();
    }

    getSize () {
        return super.getSize();
    }

    isEmpty () {
        return super.isEmpty();
    }

    add (e) {
        super.add(e)
    }

    contains (e) {
        return super.contains(e);
    }

    remove (e) {
        return super.remove(e);
    }
}